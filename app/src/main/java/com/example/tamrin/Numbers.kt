package com.example.tamrin

fun main(){

    var money:Float=5.99f
    println("Money is $money ${money::class.java}")

    var life:Long=42L
    println("life is $life ${life::class.java}")

    var cats:Byte=4
    println("cats is $cats ${cats::class.java}")

    val price=2.35
    println("price is $price ${price::class.java}")
}